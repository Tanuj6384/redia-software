//String
var myName ='tanuj';
console.log("my name is "  + myName);   // my name is tanuj
console.log("data type is "+ (typeof myName));  //data type is string


// numbers
var marks = 34;
console.log(marks);  //34
console.log(typeof marks); // number

// boolean
var isdriver = true;
console.log(typeof isdriver);  //boolean

// null
var nullVar = null;
console.log("data type is "+ (typeof nullVar)); //data type is object

// undefined
var undef =undefined;
console.log("data type is "+ (typeof undef)); //data type is undefined
